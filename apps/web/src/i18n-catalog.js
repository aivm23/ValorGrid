function mergeTranslations(...catalogs) {
  return catalogs.reduce(
    (acc, catalog) => ({
      es: { ...acc.es, ...(catalog.es || {}) },
      en: { ...acc.en, ...(catalog.en || {}) },
    }),
    { es: {}, en: {} },
  );
}

